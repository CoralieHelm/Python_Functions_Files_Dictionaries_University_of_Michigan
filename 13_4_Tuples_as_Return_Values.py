#Task 1 Define a function called information that takes as input, the variables name, birth_year, fav_color, and hometown. It should return a tuple of these variables in this order.

def information(name, birth_year, fav_color, hometown):
    return name, birth_year, fav_color, hometown



#Task 2 Define a function called info with the following required parameters: name, age, birth_year, year_in_college, and hometown. The function should return a tuple that contains all the inputted information.

def info(name, age, birth_year, year_in_college, hometown):
    return name, age, birth_year, year_in_college, hometown
